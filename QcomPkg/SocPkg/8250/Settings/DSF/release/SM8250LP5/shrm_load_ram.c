void shrm_full_load_ram ( uint32 shrm_ram_base_addr,  uint32 *shrm_ram_data,  uint16 data_length )
{
    uint16 reg;
    //data_length is number of bytes
    uint16 reg_count = data_length/4;
    for (reg = 0; reg < reg_count; reg++) 
    {
        out_dword(shrm_ram_base_addr+(4*reg), shrm_ram_data[reg]); 
    }
}
 
void SHRM_Full_Load_DRAM (void)
{
    uint16 data_length = 0;
    uint32 shrm_ram_base_addr = 0;
    uint32 *shrm_dram_data_addr = NULL;
 
//Loading .rodata section
    data_length = RODATA_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = SEQ_DDR_SS_SHRM_MEM_SHRM_OFFSET + RODATA_SECTION_OFFSET_FULL;
    shrm_dram_data_addr = shrm_full_dram_base_data;
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_dram_data_addr, data_length );
 
//Loading .data section 
    data_length = DATA_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = SEQ_DDR_SS_SHRM_MEM_SHRM_OFFSET + DATA_SECTION_OFFSET_FULL;
    shrm_dram_data_addr += (RODATA_SECTION_LENGTH_FULL >>2);
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_dram_data_addr, data_length );
 
//Loading .bss section 
    data_length = BSS_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = SEQ_DDR_SS_SHRM_MEM_SHRM_OFFSET + BSS_SECTION_OFFSET_FULL;
    shrm_dram_data_addr += (DATA_SECTION_LENGTH_FULL >>2);
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_dram_data_addr, data_length );
}
 
void SHRM_Full_Load_IRAM (void)
{
    uint16 data_length = 0;
    uint32 shrm_iram_base_addr = SEQ_DDR_SS_SHRM_MEM_SHRM_OFFSET + SHRM_IRAM_OFFSET;
    uint32 shrm_ram_base_addr = 0;
    uint32 *shrm_iram_data_addr = NULL;
 
//Loading RESETVECTOR_TEXT section
    data_length = RESETVECTOR_TEXT_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = shrm_iram_base_addr + RESETVECTOR_TEXT_SECTION_OFFSET_FULL;
    shrm_iram_data_addr = shrm_full_iram_base_data;
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_iram_data_addr, data_length );
 
//Loading DYANMICVECTORS_S section
    data_length = DYANMICVECTORS_S_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = shrm_iram_base_addr + DYANMICVECTORS_S_SECTION_OFFSET_FULL;
    shrm_iram_data_addr += (RESETVECTOR_TEXT_SECTION_LENGTH_FULL>>2) ;
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_iram_data_addr, data_length );
 
//Loading MAIN_SEGMENT section
    data_length = MAIN_SEGMENT_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = shrm_iram_base_addr + MAIN_SEGMENT_SECTION_OFFSET_FULL;
    shrm_iram_data_addr += (DYANMICVECTORS_S_SECTION_LENGTH_FULL>>2) ;
    shrm_full_load_ram ( shrm_ram_base_addr,  shrm_iram_data_addr, data_length );
 
}
 
 
void SHRM_Full_Load_SRAM (void)
{
    //Loading SHRM DRAM
    SHRM_Full_Load_DRAM();
 
    //Loading SHRM IRAM 
    SHRM_Full_Load_IRAM();
 
    //Put SHRM into RUN state 
    out_dword(SEQ_DDR_SS_SHRM_CSR_SHRM_CSR_OFFSET+0x1000, 0); 
 
}
 
